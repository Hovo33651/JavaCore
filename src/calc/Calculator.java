package calc;

public class Calculator {

    public int gumarum(int a, int b) {
        int c = a + b;
        return c;
    }

    public int hanum(int a, int b) {
        int c = a - b;
        return c;
    }

    public double bajanum(double a, double b) {
        double c = a / b;
        return c;
    }

   public int bazmapatkum(int a, int b) {
        int c = a * b;
        return c;
    }
    }



